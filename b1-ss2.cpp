#include<stdio.h>
int main(){
	int a=10;//Khai bao va khai tao bien a la kieu du lieu so nguyen, gia tri la 10
	printf("a=%d\n",a);	
	char b='B';//Khai bao va khai tao bien b la kieu du lieu ki tu, gia tri la B 
	printf("b=%c\n",b);
	float c=3.14;//Khai bao va khai tao bien b la kieu du lieu so thuc, gia tri la 3.14
	printf("c=%f\n",c);
	double d=3.1444444;//Khai bao va khai tao bien b la kieu du lieu so thuc, gia tri la 3.144444 
	printf("d=%lf\n",d);
	return 0;
	
}
